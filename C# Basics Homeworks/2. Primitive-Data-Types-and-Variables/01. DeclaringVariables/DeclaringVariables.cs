using System;

class DeclaringVariables
{
    static void Main()
    {
        ushort firstNumber = 52130;
        sbyte secondNumber = -115;
        int thirdNumber = 4825932;
        byte fourthNumber = 97;
        short fifthNumber = -10000;

        // Printing the variables on the console
        Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}", firstNumber, secondNumber, thirdNumber,
            fourthNumber, fifthNumber);
    }
}
